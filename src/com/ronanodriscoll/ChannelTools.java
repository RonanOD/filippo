import java.io.IOException;
import java.nio.ByteBuffer;
import java.nio.channels.ReadableByteChannel;
import java.nio.channels.WritableByteChannel;

/**
 * Fast file copy utility. Taken from http://thomaswabner.wordpress.com/2007/10/09/fast-stream-copy-using-javanio-channels/
 * 
 * @author Thomas Wabner
 */
public final class ChannelTools {
  public static void fastChannelCopy(final ReadableByteChannel src,
      final WritableByteChannel dest) throws IOException {
    final java.nio.ByteBuffer buffer = ByteBuffer.allocateDirect(16 * 1024);
    while (src.read(buffer) != -1) {
      // prepare the buffer to be drained
      buffer.flip();
      // write to the channel, may block
      dest.write(buffer);
      // If partial transfer, shift remainder down
      // If buffer is empty, same as doing clear()
      buffer.compact();
    }
    // EOF will leave buffer in fill state
    buffer.flip();
    // make sure the buffer is fully drained.
    while (buffer.hasRemaining()) {
      dest.write(buffer);
    }
  }
}